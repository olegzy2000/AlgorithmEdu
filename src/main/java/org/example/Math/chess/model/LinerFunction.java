package org.example.Math.chess.model;

public class LinerFunction {
    private float k;
    private float b;

    public LinerFunction() {
    }

    public LinerFunction(float k, float b) {
        this.k = k;
        this.b = b;
    }

    public float getK() {
        return k;
    }

    public void setK(float k) {
        this.k = k;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }

}
